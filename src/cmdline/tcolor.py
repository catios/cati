''' ANSI terminal colors '''

HEADER = '\033[95m'
OKBLUE = '\033[94m'
OKGREEN = '\033[92m'
WARNING = '\033[93m'
FAIL = '\033[91m'
ENDC = '\033[0m'
BOLD = '\033[1m'
UNDERLINE = '\033[4m'

def disable():
    from cmdline import tcolor
    tcolor.HEADER = ''
    tcolor.OKBLUE = ''
    tcolor.OKGREEN = ''
    tcolor.WARNING = ''
    tcolor.FAIL = ''
    tcolor.ENDC = ''
    tcolor.BOLD = ''
    tcolor.UNDERLINE = ''
